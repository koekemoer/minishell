#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char		*new;
	size_t		i;

	if (!f || !s)
		return (NULL);
	i = 0;
	new = ft_strdup(s);
	if (new == NULL)
		return (NULL);
	while (s[i])
	{
		new[i] = f(s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
